package Loopweek;

public class Table10 {
    public static void main(String[] args) {
        // created 10times table using concatenation
        int i =10;
        for(int a=1; a<=10; a++){
            System.out.println(i+"x"+a+"="+a*i);
        }
    // output will be 10times table 10x1 to 10x10

    }
}
