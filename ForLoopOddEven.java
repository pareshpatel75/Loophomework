package Loopweek;

public class ForLoopOddEven {
    public static void main(String[] args) {

        // create odd number using for loop 1-21
       for(int i=0; i<21; i++){
           System.out.println(i);
           i++;
           //output will be even numbers 1-21
                }
       for (int i=1; i<22; i++){
           System.out.println(i);
           i++;
            }
       // output will be odd numbers from 1-22
        }
    }










