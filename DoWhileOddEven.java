package Loopweek;

public class DoWhileOddEven {
    public static void main(String[] args) {
        // created programme to get odd and even numbers using do while loop
        int i = 2;
        do {
           System.out.println(i);
           i+=2;
           // outcome will be even numbers from 1-20
        }
        while (i <= 21);
        // define odd number from 1-21
        int j=1;
        do{
            System.out.println(j);
            j+=2;}
        while(j<=21);
        // outcome will be odd
    }
}















